# Media Escolar


a = float(input('Digite sua primeira nota? '))
b = float(input('Digite sua segunda nota? '))
c = float(input('Digite sua terceira nota? '))
d = a * 0.3 + b * 0.3 + c * 0.4
if d >= 5:
    print('Você foi Aprovado', d)
if d < 5:
    print('Você foi Reprovado',d)
if d== 5:
    print('Quase passou arrastado ordinário!!!',d)
    
